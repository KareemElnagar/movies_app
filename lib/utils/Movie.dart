class Movie {
  final String title;
  final String genre;
  final String duration;
  final String summary;
  final String imagePath;
  final double rating;
  final int year;

  Movie({
    required this.title,
    required this.genre,
    required this.duration,
    required this.summary,
    required this.imagePath,
    required this.rating,
    required this.year,
  });
}